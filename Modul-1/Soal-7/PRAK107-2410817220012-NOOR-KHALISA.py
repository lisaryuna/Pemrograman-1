# Sisi-sisi tanah
sisi1 = 4
sisi2 = 5
sisi3 = 7

# Keliling Tanah Pak Dengklek
keliling = sisi1 + sisi2 + sisi3

# Harga per meter
harga = 85000

# Biaya yang diperlukan
biaya = keliling * harga

# Output
print("Diketahui")
print(f"Panjang sisi segitiga berturut-turut adalah {sisi1}, {sisi2}, dan {sisi3}")
print(f"Keliling Tanah Pak Dengklek adalah {keliling}")
print(f"Harga tanah Per Meter adalah {harga}")
print("Jawaban:")
print(f"Biaya yang diperlukan Pak Dengklek adalah: Rp {biaya}")