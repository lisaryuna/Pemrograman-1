# Variabel
a = 4
b = 8
c = 3

# Pengecekan
sama_dengan = (a == b)
lebih_besar_dari = (b > c)
tidak_sama_dengan = (a != c)

# Output
print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")
print(f"Apakah a sama dengan b? Jawabannya adalah {int(sama_dengan)}")
print(f"Apakah b lebih besar dari c? Jawabannya adalah {int(lebih_besar_dari)}")
print(f"Apakah a tidak sama dengan c? Jawabannya adalah {int(tidak_sama_dengan)}")